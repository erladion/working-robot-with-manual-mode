int irRead(uint8_t channel){
	// Read from ADMUX channel
	channel &= 0b00000011;
	ADMUX &= 0b11111000;
	ADMUX |= channel;
	
	// Start first conversion (useless)
	sbi(ADCSRA, ADSC);
	loop_until_bit_is_clear(ADCSRA,ADSC);
	
	//_delay_ms(5);
	
	// Start second conversion
	sbi(ADCSRA, ADSC);
	loop_until_bit_is_clear(ADCSRA,ADSC);
	
	int low = ADCL;
	int highAndLow = (ADCH << 8) | low;	
	
	return highAndLow;
}

void irInit(){
	
	//DDRA = 0x00;
	// Use AREF = AVCC
	sbi(ADMUX, REFS0);
	cbi(ADMUX, REFS1);	
	
	DIDR0 = 0xFF;
	
	// Enable ADC
	sbi(ADCSRA, ADEN);
	
	// Set prescalar to 128
	//  ADPS2	ADPS1	ADPS0
	//		0		0		0	2
	//		0		0		1	2
	//		0		1		0	4
	//		0		1		1	8
	//		1		0		0	16
	//		1		0		1	32	
	//		1		1		0	64
	//		1		1		1	128	
	sbi(ADCSRA,ADPS0);
	sbi(ADCSRA,ADPS1);	
	sbi(ADCSRA,ADPS2);	
}

